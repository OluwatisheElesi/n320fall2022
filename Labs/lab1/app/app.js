 
 //class call instrument
 class Instrument {
    // loudness, verb, and familiy in constructor aka volumne 
    constructor (loudness, family, verb) {
        this.loudness = loudness;
        this.family = family;
        this.verb = verb;
    }
 }


class Woodwind extends Instrument {
    constructor (family, verb) {
        super (loudness);
        this.woodwind = wind;
    }

}


class Purcussion extends Instrument {
    constructor (family, verb) {
        super (loudness);
        this.purcussion = drums;

    }
}


class String extends Instrument {
    constructor (family, verb) {
        super (loudness);
        this.string = string;
    }

}


